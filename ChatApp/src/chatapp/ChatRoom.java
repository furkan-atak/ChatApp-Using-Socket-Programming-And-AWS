    public void getUsers(ArrayList<String> userNames) {
        
        System.out.println("Username Size: " + userNames.size());
        dlm.removeAllElements();
        userList.removeAll();
        for (String usrName : userNames) {
            dlm.addElement(usrName);
        }
        userList.setModel(dlm);
    }
    
